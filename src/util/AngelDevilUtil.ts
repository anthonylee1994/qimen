export const AngelDevilUtil = Object.freeze({
    getAngelDevilMap: (時支: 地支): Record<地支, 神煞[]> => {
        const angelDevils = Object.keys(神煞表) as 神煞[];
        const angelDevilOf = (value: 地支) => angelDevils.filter(name => 神煞表[name as 神煞][時支] === value);

        return {
            子: angelDevilOf("子"),
            丑: angelDevilOf("丑"),
            寅: angelDevilOf("寅"),
            卯: angelDevilOf("卯"),
            辰: angelDevilOf("辰"),
            巳: angelDevilOf("巳"),
            午: angelDevilOf("午"),
            未: angelDevilOf("未"),
            申: angelDevilOf("申"),
            酉: angelDevilOf("酉"),
            戌: angelDevilOf("戌"),
            亥: angelDevilOf("亥"),
        };
    },
    isAngel: (神煞: 神煞): boolean => {
        return angels.some(_ => 神煞.startsWith(_));
    },
    isDevil: (神煞: 神煞): boolean => {
        return devils.some(_ => 神煞.startsWith(_));
    },
});
